################################################################################
#' Load packages
################################################################################

library(tidyverse)

################################################################################
#' Load data
################################################################################

windspeed <- readRDS(file = "pipeline/vine_copula/simulate_windspeed_2/windspeed_10000_daily.rds")
################################################################################
#' Functions
################################################################################

speed_to_power <- function(x){
  
  C_r <- 15
  power <- vector()
  
  for(i in 1:length(x)){
  if(x[i] < 3 | x[i] > 25){
    power[i] <- 0
  } else if(x[i] >= 3 & x[i] <= 10.59){
    power[i] <- (x[i]^3 - 3^3)/(10.59^3 - 3^3)
  } else{
    power[i] <- 1
  }
  }
  output <- C_r*power
  return(output)
}

################################################################################
#' Convert windspeed to windpower
################################################################################

windpower <- apply(windspeed, 2, FUN = speed_to_power)

################################################################################
#' Save file
################################################################################

saveRDS(windpower, file = "pipeline/vine_copula/simulate_windpower/windpower_10000_daily.rds")
